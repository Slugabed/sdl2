Map::Map() {
    dirt = TextManager::LoadTexture("Resources/dirt.png");
    grass = TextManager::LoadTexture("Resources/grass.png");
    water = TextManager::LoadTexture("Resources/water.png");

    LoadMap(lvl1);

    src.x = src.y = 0;
    dest.x = dest.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;
}

void Map::LoadMap(MapArr arr) {
    map = std::move(arr);
}

void Map::DrawMap() {
    for(int row=0; row < HEIGHT; row++) {
        for(int col=0; col < WIDTH; col++) {
            dest.x = col * 32;
            dest.y = row * 32;
            switch (map[row][col]) {
                case 0:
                    TextManager::Draw(water, src, dest);
                    break;
                case 1:
                    TextManager::Draw(grass, src, dest);
                case 2:
                    TextManager::Draw(dirt, src, dest);
                default:
                    break;
            }
        }
    }
}
